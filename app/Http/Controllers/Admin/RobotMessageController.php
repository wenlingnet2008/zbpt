    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $robot_message = RobotMessage::findOrFail($id);
        $robot_message->delete();
        return response()->json(['message'=>'删除成功']);
